function createDSLChart() {
    const el_debtSubjectToLimit = document.getElementById('graph--debt-subject-to-limit');

    let limitChart = new Chart(el_debtSubjectToLimit, {
        type: 'line',
        data: {
            labels: labels_dates,
            datasets: [
                {
                    label: 'Debt Subject to Limit',
                    data: data_debtSubjectToLimit,
                    borderColor: 'rgba(3, 121, 202, 1)',
                    borderWidth: 3,
                    radius: 0, //width circle at each intersection
                    backgroundColor: 'rgba(3, 121, 202, 1)',
                    fill: false
                },
                {
                    label: 'Public Debt Outstanding',
                    data: data_publicDebtOutstanding,
                    borderColor: 'rgba(105, 165, 48, 1)',
                    borderWidth: 3,
                    radius: 0,
                    backgroundColor: 'rgba(105, 165, 48, 1)',
                    fill: false
                },
                {
                    label: 'Statutory Debt Limit', //Statutory Debt Limit* if there's a notation about the debt limit
                    data: data_statutoryDebtLimit,
                    borderColor: 'rgba(240, 96, 0, 1)',
                    borderWidth: 3,
                    radius: 0,
                    backgroundColor: 'rgba(240, 96, 0, 1)',
                    fill: false
                }
            ]
        },
        options: {
            plugins: {
                title: {
                    display: true,
                    text: 'Debt Subject to Limit',
                    font: {
                        size: 24,
                        color: '#2e2e2e'
                    }
                },
                tooltip: {
                    intersect: false,
                    mode: 'index',
                    callbacks: {
                        label: function (context) {
                            let label = context.dataset.label || '';
                            if (label) {
                                label += ': ';
                            }
                            if (context.parsed.y !== null) {
                                label += new Intl.NumberFormat('en-US', {
                                    style: 'currency',
                                    currency: 'USD'
                                }).format(debtLimitRounder(context.parsed.y));
                            }
                            return label;
                        }
                    }
                }
            },
            scales: {
                yAxes: {
                    title: {
                        display: true,
                        text: 'Billions',
                        font: {
                            size: 16,
                            color: '#2e2e2e'
                        }
                    },
                    suggestedMin: 26000000000000,
                    suggestedMax: 34000000000000,
                    ticks: {
                        // format for y-axis label
                        callback: function (value, index, values) {
                            return formatCurrency(value, 5);
                        },
                        // ticks acts like a zoom for the graph (only for lines behind)
                        maxTicksLimit: 6
                    }
                },
                xAxes: {
                    title: {
                        type: 'time',
                        time: {
                            unit: 'month'
                        },
                        display: true,
                        text: 'Months',
                        font: {
                            size: 16,
                            color: '#2e2e2e'
                        }
                        /* Need an adapter for time library to work, distribution tag is now renamed timeseries, read chartjs.org>axes>timeCartesian
                        distribution: 'series',
                        type: 'time',
                        time: {
                            unit: 'month',
                            displayFormats: {
                                month: 'MM/DD/YY'
                            }
                        },
                         */
                    },
                    ticks: {
                        callback: function (value) {
                            //Value is the position in the list of the item we are looking at
                            //currDate is the actual date, pulled from the list
                            let currDate = labels_dates[value];
                            if (value === 0 || ((value + 1) % 10 === 0 && (value + 1) <= (labels_dates.length - 5))) {
                                return currDate;
                            }
                            if ((value + 1) === labels_dates.length) {
                                return currDate;
                            }
                        }
                    }
                }
            }

        }
    })
}

